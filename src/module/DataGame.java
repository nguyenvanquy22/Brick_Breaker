package module;

public class DataGame {
	private String timePlayGame;
	private int levelGame;
	private int score;
	private int result;
	
	public String getTimePlayGame() {
		return timePlayGame;
	}
	public void setTimePlayGame(String timePlayGame) {
		this.timePlayGame = timePlayGame;
	}
	public int getLevelGame() {
		return levelGame;
	}
	public void setLevelGame(int levelGame) {
		this.levelGame = levelGame;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	public int getResult() {
		return result;
	}
	public void setResult(int result) {
		this.result = result;
	}
	
}
